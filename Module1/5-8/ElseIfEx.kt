fun main()
{

    var marks = 67

    if(marks>=70)
    {
        print("A Grade")
    }
    else if(marks>=60)
    {
        print("B Grade")
    }
    else if(marks>=50)
    {
        print("C Grade")
    }
    else if(marks>=40)
    {
        print("D Grade")
    }
    else
    {
        print("Fail")
    }


}

/*
    70-A
    60-B
    50-C
    40-D
    Fail
        */
